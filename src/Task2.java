public class Task2 {
    /**
     * @param args
     */
    public static void main(String[] args) {
        int n = 10;
        int sum = 0;
        for (int i = 1; i <= n; i++) {
            sum += i;
        }
        System.out.println("The sum of numbers from one to " + n + " is equal to " + sum);
    }
}
